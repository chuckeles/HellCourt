/// <summary>
///   Defines a sin.
/// </summary>
public struct Sin {

  /// <summary>
  ///   How much mental pain is needed for the sin.
  /// </summary>
  public float MentalPainNeeded;

  /// <summary>
  ///   How much physical pain is needed for the sin.
  /// </summary>
  public float PhysicalPainNeeded;

  /// <summary>
  ///   What do humans say.
  /// </summary>
  public string SayLine;

}
